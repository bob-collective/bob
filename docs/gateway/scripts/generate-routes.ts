import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";

const ROUTES_API = "https://gateway-api-mainnet.gobob.xyz/v1/get-routes";
const NON_EVM: Record<string, string> = { bitcoin: "BTC", aptos: "WBTC" };
const cap = (s: string) => (s === "bsc" ? "BSC" : s[0].toUpperCase() + s.slice(1));

async function main() {
  const routes: { srcChain: string; srcToken: string; dstChain: string; dstToken: string }[] =
    await fetch(ROUTES_API).then((r) => r.json());

  const tokenlistPath = resolve(dirname(new URL(import.meta.url).pathname), "../../../tokenlist/tokenlist.json");
  const { tokens } = JSON.parse(readFileSync(tokenlistPath, "utf-8"));

  const symbol = new Map<string, string>();
  for (const t of tokens) symbol.set(t.address.toLowerCase(), t.symbol);

  const sym = (chain: string, addr: string) => {
    const s = NON_EVM[chain] ?? symbol.get(addr.toLowerCase());
    if (!s) console.warn(`  Unknown: ${addr} on ${chain}`);
    return s ?? "Unknown";
  };

  // Group by source chain
  const groups = Map.groupBy(routes, (r) => r.srcChain);

  let mdx = `---
title: "Supported Routes & Assets"
description: "All supported chains and token routes available through the BOB Gateway."
icon: "route"
---

{/* Auto-generated by docs/gateway/scripts/generate-routes.ts — do not edit manually. */}

Auto-generated from the live [\`/v1/get-routes\`](/api-reference/overview) endpoint
and the [BOB tokenlist](https://github.com/bob-collective/bob/blob/master/tokenlist/tokenlist.json).
Last updated: ${new Date().toISOString().split("T")[0]}

`;

  for (const chain of [...groups.keys()].sort()) {
    const rows = groups.get(chain)!;
    rows.sort((a, b) => a.dstChain.localeCompare(b.dstChain) || a.srcToken.localeCompare(b.srcToken));

    mdx += `## From ${cap(chain)}\n\n`;
    mdx += `| Source Token | Destination Chain | Destination Token |\n|---|---|---|\n`;
    for (const r of rows)
      mdx += `| ${sym(r.srcChain, r.srcToken)} | ${cap(r.dstChain)} | ${sym(r.dstChain, r.dstToken)} |\n`;
    mdx += `\n`;
  }

  const out = resolve(dirname(new URL(import.meta.url).pathname), "../gateway/supported-routes.mdx");
  mkdirSync(dirname(out), { recursive: true });
  writeFileSync(out, mdx);
  console.log(`Generated ${out} (${routes.length} routes, ${tokens.length} tokens)`);
}

main().catch((e) => { console.error(e); process.exit(1); });
